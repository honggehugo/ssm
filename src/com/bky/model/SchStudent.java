package com.bky.model;

public class SchStudent {
	private String schoolId; 	//学校id
	private String schoolName; 	//学校名
	private String gradCount; 	//毕业生数量
	private String gradNum; 	//就业毕业生总数
	
	
	public String getSchoolId() {
		return schoolId;
	}
	public void setSchoolId(String schoolId) {
		this.schoolId = schoolId;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getGradNum() {
		return gradNum;
	}
	public void setGradNum(String gradNum) {
		this.gradNum = gradNum;
	}
	public String getGradCount() {
		return gradCount;
	}
	public void setGradCount(String gradCount) {
		this.gradCount = gradCount;
	}
	
	



    
}